				<h2>Post-Sale <span>(Engage)</span></h2>
				<ul>
					<li>The buyer journey doesn't end once a final decision on a solution has been made; for companies serving customers, that journey is just beginning. This is when personalization, customer experience and commitment to education are most critical.</li>
					<li>In-person sales interaction is most important at this stage (38%), with vendor websites a close second (32%).</li>
					<li>It's up to the vendor to provide ways to educate their new customers on use, integration and adoption; but buyers themselves must also devote consistent resources to integration and adoption to ensure long-term success.</li>
